// Busca um livro pelo ID exato (ex: "clean-code")
function buscarLivroPorId(id) {
  return LIVROS_CATALOGO.find((livro) => livro.id === id);
}

// Busca um livro pelo título — converte pra slug antes de procurar
// Ex: "Clean Code" vira "clean-code" e aí faz o find
function buscarLivroPorTitulo(titulo) {
  return buscarLivroPorId(criarId(titulo));
}

// Monta o objeto do livro só com o que o carrinho precisa
// Evita carregar dados desnecessários como "sobre", "ficha técnica", etc.
function livroParaCarrinho(livro) {
  return {
    id: livro.id,
    titulo: livro.titulo,
    autor: livro.autor,
    categoria: livro.categoria,
    preco: livro.preco,
    imagem: livro.imagem,
    quantidade: 1,
  };
}

